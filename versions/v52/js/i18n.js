let currentLang = localStorage.getItem('seo_lang') || 'en';

function switchLang(lang) {
  currentLang = lang;
  localStorage.setItem('seo_lang', lang);
  document.documentElement.lang = lang;
  
  // Update data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (translations[lang] && translations[lang][key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = translations[lang][key];
      } else {
        el.innerHTML = translations[lang][key];
      }
    }
  });

  // Update lang buttons active state
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Update nav login button text
  const loginBtn = document.getElementById('navLoginBtn');
  if (loginBtn && !loginBtn.dataset.translated) {
    loginBtn.innerHTML = translations[lang]['nav.login'];
  }

  // Update show-more button
  const showBtn = document.getElementById('showMoreBtn');
  if (showBtn) {
    const isOpen = showBtn.classList.contains('open');
    showBtn.innerHTML = isOpen
      ? translations[lang]['footer.showLess'] + ' <span class="arrow">↑</span>'
      : translations[lang]['footer.showMore'] + ' <span class="arrow">↓</span>';
  }
}

// Helper: translate a key
function t(key) {
  if (translations[currentLang] && translations[currentLang][key] !== undefined) {
    return translations[currentLang][key];
  }
  if (translations['en'] && translations['en'][key] !== undefined) {
    return translations['en'][key];
  }
  return key;
}

